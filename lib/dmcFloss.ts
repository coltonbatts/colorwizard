/**
 * Finds the closest DMC embroidery floss colors to a given RGB color
 * using Delta E (CIEDE2000) for perceptual accuracy.
 *
 * @param rgb - The target color in RGB format
 * @param count - Number of matches to return (default: 5)
 * @returns Array of closest DMC color matches with distance, similarity, and confidence labels
 */
export function findClosestDMCColors(
  rgb: { r: number; g: number; b: number },
  count: number = 5
): DMCMatch[] {
  // Convert target to Lab once
  const targetLab = rgbToLab(rgb.r, rgb.g, rgb.b)

  // Calculate distance for each DMC color using Delta E (Lab)
  const matches = DMC_COLORS.map((dmcColor) => {
    // Lazy calculate Lab if not present (simple memoization)
    if (!dmcColor.lab) {
      dmcColor.lab = rgbToLab(dmcColor.rgb.r, dmcColor.rgb.g, dmcColor.rgb.b)
    }

    // Calculate Delta E
    const distance = deltaE(targetLab, dmcColor.lab!)

    // Calculate generic similarity for UI (invert approximate dE)
    // dE 0 = 100%, dE 100 = 0% (clamped)
    const similarity = Math.max(0, 100 - distance)

    // Get UI-ready confidence label
    const { label, color, bgColor } = getMatchConfidence(distance)

    return {
      ...dmcColor,
      distance,
      similarity,
      confidenceLabel: label,
      confidenceColor: color,
      confidenceBgColor: bgColor
    }
  })

  // Sort by distance (closest first) and return top matches
  return matches.sort((a, b) => a.distance - b.distance).slice(0, count)
}
